// Command gen_codecs generates codecs_gen.go: the long-tail single-byte
// character codecs (ISO-8859-N, Windows-125N) as 256-rune tables, read from
// the authoritative Unicode Consortium mapping files so the data is exact
// rather than hand-transcribed.
//
// Run from the package directory (network required):
//
//	go generate ./internal/lib/encoding/
//
// or from anywhere with an explicit output path:
//
//	go run ./internal/lib/encoding/gen_codecs.go internal/lib/encoding/codecs_gen.go
//
// Only `ascii` (7-bit, with custom out-of-range errors) and `ebcdic`
// (IBM-1047, not in the standard Unicode mapping path) stay hand-written in
// codecs.go; every ISO-8859 and Windows single-byte codec is generated here
// so the tables come from one authoritative source. ISO-8859-12 was never
// standardised.
