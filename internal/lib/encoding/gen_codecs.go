// tableVar turns a codec name into its Go var identifier:
// "iso-8859-2" -> "iso88592Table", "windows-1250" -> "windows1250Table".
func tableVar(codec string) string {
	return strings.ReplaceAll(codec, "-", "") + "Table"
}

// fetchTable reads a Unicode mapping file (lines "0xBB<tab>0xUUUU # NAME";
// a byte with no code point is undefined) into a 256-rune table, with
// undefined positions left as U+FFFD (utf8.RuneError).
func fetchTable(url string) (*[256]rune, error) {
	var t [256]rune
	for i := range t {
		t[i] = 0xFFFD
	}
	resp, err := http.Get(url)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()
	if resp.StatusCode != 200 {
		return nil, fmt.Errorf("HTTP %d", resp.StatusCode)
	}
	sc := bufio.NewScanner(resp.Body)
	for sc.Scan() {
		line := sc.Text()
		if i := strings.IndexByte(line, '#'); i >= 0 {
			line = line[:i]
		}
		f := strings.Fields(line)
		if len(f) < 2 {
			continue // blank line or an undefined byte (no code point)
		}
		b, err := strconv.ParseUint(strings.TrimPrefix(f[0], "0x"), 16, 16)
		if err != nil || b > 0xFF {
			continue
		}
		cp, err := strconv.ParseUint(strings.TrimPrefix(f[1], "0x"), 16, 32)
		if err != nil {
			continue
		}
		t[b] = rune(cp)
	}
	return &t, sc.Err()
}

func main() {
	out := "codecs_gen.go"
	if len(os.Args) > 1 {
		out = os.Args[1]
	}

	var buf bytes.Buffer
	fmt.Fprintln(&buf, "// SPDX-License-Identifier: LGPL-3.0-only")
	fmt.Fprintln(&buf, "// Copyright (C) 2026 <developer@mplx.eu>")
	fmt.Fprintln(&buf)
	fmt.Fprintln(&buf, "// Code generated by gen_codecs.go; DO NOT EDIT.")
	fmt.Fprintln(&buf, "// Source: Unicode Consortium mapping files (https://www.unicode.org/Public/MAPPINGS/).")
	fmt.Fprintln(&buf)
	fmt.Fprintln(&buf, "package encodinglib")
	fmt.Fprintln(&buf)
	fmt.Fprintln(&buf, "func init() {")
	for _, g := range gen {
		fmt.Fprintf(&buf, "\tregisterTableCodec(%q, &%s, true)\n", g.name, tableVar(g.name))
	}
	fmt.Fprintln(&buf, "}")
	fmt.Fprintln(&buf)

	for _, g := range gen {
		t, err := fetchTable(g.url)
		if err != nil {
			log.Fatalf("%s (%s): %v", g.name, g.url, err)
		}
		fmt.Fprintf(&buf, "var %s = [256]rune{\n", tableVar(g.name))
		for i := 0; i < 256; i += 8 {
			buf.WriteByte('\t')
			for j := i; j < i+8; j++ {
				fmt.Fprintf(&buf, "0x%04X, ", t[j])
			}
			buf.WriteByte('\n')
		}
		fmt.Fprintln(&buf, "}")
		fmt.Fprintln(&buf)
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("gofmt: %v", err)
	}
	if err := os.WriteFile(out, src, 0o644); err != nil {
		log.Fatal(err)
	}
	fmt.Printf("wrote %s (%d codecs)\n", out, len(gen))
}
